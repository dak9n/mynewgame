/**
 * Порядок отрисовки существ. Общий для игрока и монстров — в этом весь смысл
 * файла.
 *
 * Пауки раньше считали глубину как sprite.y, то есть в мировых пикселях (0..752
 * в лесу), а игрок — постоянной 300. Шкалы разные, и последствия были видны
 * глазом: паук, подошедший с севера, рисовался ПОВЕРХ героя, потому что 408
 * больше 300. А паук южнее 16-го ряда тайлов (y > 260) перекрывал ещё и слои
 * карты — заходя за ствол, полз по кроне.
 *
 * DEPTH_ABOVE — поверх всей карты: так ходят по траве, камням, пням и тростнику,
 * не ныряя за них.
 * DEPTH_BEHIND — под слоями объектов: включается, только когда существо зашло за
 * большое дерево.
 *
 * Числа привязаны к текущему порядку слоёв (шаг 10, в лесу 27 слоёв -> 0..260):
 * переставят слои — пересчитать.
 */

export const DEPTH_ABOVE = 300;
export const DEPTH_BEHIND = 215;

/**
 * Глубина существа в точке (x, y) мира.
 *
 * Дробная добавка по Y сортирует существ МЕЖДУ СОБОЙ: кто южнее, тот ближе к
 * зрителю. Делитель большой намеренно — добавка обязана остаться меньше шага
 * слоёв карты (10), иначе существо перескочило бы в чужой слой и снова полезло
 * бы на крону.
 */
export function creatureDepth(
  x: number,
  y: number,
  tallObjects: Map<number, number>,
  mapWidth: number,
  tileW: number,
  tileH: number,
): number {
  const tx = Math.floor(x / tileW);
  const ty = Math.floor(y / tileH);
  const baseY = tallObjects.get(ty * mapWidth + tx);

  // За деревом — если ноги существа выше его основания: тогда крона его накроет.
  const behind = baseY !== undefined && y < baseY;
  return (behind ? DEPTH_BEHIND : DEPTH_ABOVE) + y / 10000;
}
