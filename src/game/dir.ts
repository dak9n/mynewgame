/**
 * Направление взгляда и порядок рядов в спрайт-листах.
 *
 * Отдельным файлом от anims.ts намеренно: там код лезет в canvas браузера, а
 * это знание нужно и чистой боевой математике, которая проверяется тестами без
 * браузера.
 */

export type Dir = 'down' | 'left' | 'right' | 'up';

/**
 * Порядок рядов у мечника и у пауков РАЗНЫЙ — это не опечатка, а факт:
 * проверено по картинкам (у мечника профили в рядах 1 и 2, у пауков — 2 и 3,
 * что подтверждается зеркальностью силуэтов).
 *
 * Перепутать их — получить паука, который ползёт на тебя боком.
 */
export const DIRS_HERO: Dir[] = ['down', 'left', 'right', 'up'];
export const DIRS_MOB: Dir[] = ['down', 'up', 'left', 'right'];

/** Куда смотреть при таком движении. Горизонталь важнее: так читается лучше. */
export function dirFromVelocity(vx: number, vy: number, fallback: Dir): Dir {
  if (!vx && !vy) return fallback;
  if (vx) return vx > 0 ? 'right' : 'left';
  return vy > 0 ? 'down' : 'up';
}
