/**
 * Решение монстра: стоять, гнаться или возвращаться домой.
 *
 * Вынесено из monster.ts, потому что тот тянет Phaser и в тестах не грузится, а
 * ошибка тут была ровно логическая.
 *
 * ЧТО БЫЛО СЛОМАНО. Проверка поводка стояла до всего остального и звучала так:
 * «я дальше 140 от дома — иду домой». Дальность агрессии у пауков 2 и 3 больше
 * поводка (153 и 170 против 140), поэтому паук успевал уйти за поводок, всё ещё
 * видя игрока. Дальше начиналось дрожание: шаг домой -> расстояние стало меньше
 * 140 -> снова гонюсь -> снова больше 140 -> шаг домой. Паук трясся на месте, и
 * со стороны это выглядело как «упёрся в препятствие и не может обойти».
 *
 * КАК ЧИНИМ. Возвращение домой — отдельное состояние, а не проверка в лоб.
 * Ушёл за поводок — идёшь домой ДО КОНЦА, не отвлекаясь на игрока. Дошёл —
 * снова живёшь обычной жизнью. Так дрожать не на чем.
 */

export type Chase = 'idle' | 'chase' | 'leash';

export interface ChaseStats {
  /** С какого расстояния паук замечает игрока. */
  aggro: number;
  /** На каком теряет интерес. */
  deaggro: number;
  /** Как далеко от дома готов уйти. */
  leash: number;
}

export interface ChaseInput {
  mode: Chase;
  /** Квадрат расстояния до игрока. Квадраты, чтобы не считать корни. */
  toPlayer2: number;
  /** Квадрат расстояния до дома. */
  toHome2: number;
  /** Квадрат допуска: ближе этого к дому считаем, что пришёл. */
  homeTol2: number;
  /**
   * Задели монстра — стрелой или мечом. Тогда он гонится, даже если игрок дальше
   * предела агрессии: издалека из лука его иначе не расшевелить (стрела бьёт
   * дальше, чем паук замечает). Поводок всё равно главнее — уведут домой.
   */
  provoked?: boolean;
}

export function decideChase(input: ChaseInput, stats: ChaseStats): Chase {
  const { mode, toPlayer2, toHome2, homeTol2, provoked } = input;

  // Возвращение прерывать нельзя: иначе снова начнётся дрожание на поводке.
  if (mode === 'leash') return toHome2 <= homeTol2 ? 'idle' : 'leash';

  // Ушёл слишком далеко от дома — домой, что бы там ни делал игрок. Поводок
  // главнее даже провокации: иначе задетый монстр ушёл бы за игроком через всю карту.
  if (toHome2 > stats.leash * stats.leash) return 'leash';

  // Ударили — гонимся, не спрашивая про дальность агрессии.
  if (provoked) return 'chase';

  if (mode === 'chase') return toPlayer2 > stats.deaggro * stats.deaggro ? 'idle' : 'chase';
  return toPlayer2 < stats.aggro * stats.aggro ? 'chase' : 'idle';
}
