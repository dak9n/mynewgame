import { test } from 'node:test';
import assert from 'node:assert/strict';
import { creatureDepth, DEPTH_ABOVE, DEPTH_BEHIND } from './depth.ts';

const W = 90;
const T = 16;
/** Большое дерево в клетке (5,5) с основанием на y = 96. */
const tall = new Map<number, number>([[5 * W + 5, 96]]);

const depth = (x: number, y: number): number => creatureDepth(x, y, tall, W, T, T);

test('на открытом месте существо поверх карты', () => {
  // Слои леса занимают глубины 0..260 — существо обязано быть выше всех.
  const d = depth(200, 400);
  assert.ok(d >= DEPTH_ABOVE && d < DEPTH_ABOVE + 1, `${d} не в полосе «поверх»`);
  assert.ok(d > 260, 'иначе трава нарисуется поверх героя');
});

test('за большим деревом существо ныряет под слои объектов', () => {
  // Клетка дерева (5,5) -> мир 80..96 по обеим осям; основание y=96.
  const d = depth(88, 88);
  assert.ok(d >= DEPTH_BEHIND && d < DEPTH_BEHIND + 1, `${d} не в полосе «за деревом»`);
});

test('перед деревом существо снова поверх: крона не должна его накрывать', () => {
  // Тот же тайл, но ноги НИЖЕ основания — существо вышло из-за ствола.
  const d = depth(88, 95.9);
  assert.ok(d >= DEPTH_BEHIND, 'всё ещё за деревом');
  assert.ok(depth(88, 96) >= DEPTH_ABOVE, 'ровно на основании — уже перед деревом');
});

test('кто южнее, тот ближе к зрителю', () => {
  // Ради этого и добавлена дробь: иначе существа на одной полосе мигали бы
  // порядком друг относительно друга.
  assert.ok(depth(200, 500) > depth(200, 400), 'южный должен быть поверх северного');
  assert.ok(depth(200, 401) > depth(200, 400), 'даже на один пиксель');
});

test('паук с севера НЕ рисуется поверх героя', () => {
  // Тот самый баг: паук считал глубину как y (408), герой — как 300, и 408 > 300.
  const герой = depth(816, 424);
  const паук = depth(816, 408); // подошёл с севера, он ДАЛЬШЕ от зрителя
  assert.ok(паук < герой, `паук ${паук} перекрывает героя ${герой}`);
});

test('дробная добавка не выносит существо в чужой слой карты', () => {
  // Шаг слоёв карты — 10. Добавка обязана остаться меньше него, иначе существо
  // перескочит в соседний слой и снова полезет на крону.
  for (const y of [0, 752, 1120, 5000]) {
    const d = depth(200, y);
    assert.ok(d - DEPTH_ABOVE < 10, `при y=${y} добавка ${d - DEPTH_ABOVE} перескочит слой`);
  }
});

test('без больших деревьев всё поверх карты', () => {
  const d = creatureDepth(88, 88, new Map(), W, T, T);
  assert.ok(d >= DEPTH_ABOVE, 'пустая карта деревьев — прятаться не за что');
});
